async function main() {
  let count = 0;
  for (const [nameEn, prompt] of Object.entries(prompts)) {
    const result = await db.philosopher.updateMany({
      where: { nameEn },
      data: { systemPrompt: prompt },
    });
    count += result.count;
    console.log(`✓ ${nameEn}: ${result.count}`);
  }
  console.log(`Updated ${count} Chinese philosopher prompts`);
  await db.$disconnect();
}

main().catch(console.error);
